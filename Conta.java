
public class Conta {
	//atributos
	private String titular;
	private int nrConta;
	
	//construtor
	public Conta() {
		
	}
	
	// getters e setters
	public String getTitular() {
		return titular;
	}
	public void setTitular(String titular) {
		this.titular = titular;
	}
	public int getNrConta() {
		return nrConta;
	}
	public void setNrConta(int nrConta) {
		this.nrConta = nrConta;
	}
	
}
